/**
 * Theme Enum
 * 현제 다크모드 상태의 Enum입니다.
 */
export enum Theme {
  // 라이트모드인 경우
  Light = 'light',

  // 다크모드인 경우
  Dark = 'dark',
}

/**
 * Theme Enum with Auto
 * 현재 다크모드 상태 Enum에 Auto를 추가한 Enum입니다.
 */
export enum ThemeWithAuto {
  // 자동으로 설정된 경우
  Auto = 'auto',

  // 라이트모드인 경우
  Light = 'light',

  // 다크모드인 경우
  Dark = 'dark',
}
